#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	int	i;
	int	end;

	i = 0;
	if (!s1 || !set)
		return (NULL);
	while (s1[i] && ft_strchr(set, s1[i]))
		i++;
	end = ft_strlen(s1);
	while (end > 0 && (ft_strrchr(set, s1[end])))
		end--;
	return (ft_substr(s1, i, end - i + 1));
}
